// Преобразование массива в строку
// Напишите код, который преобразовывает и 
// объединяет все элементы массива в одно 
// строковое значение. Элементы массива 
// будут разделены запятой. Получите результат двумя разными методами.

const vegetables = ['Капуста', 'Репа', 'Редиска', 'Морковка'];

console.log(vegetables.join()); // Капуста,Репа,Редиска,Морковка
console.log(vegetables.toString()); // Капуста,Репа,Редиска,Морковка
